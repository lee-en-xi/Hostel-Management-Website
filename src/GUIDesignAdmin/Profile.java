    private void btnUpdateProfile1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProfile1ActionPerformed
        // TODO add your handling code here:
        Admin admin = Admin.readAdminProfile(adminID);
        //userID
        admin.setUserID(lblUserID.getText());
        //username
        admin.setUsername(txtUserName.getText());
        //email
        if (!AdminUtil.isEmail(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Email Format Error!");
            return;
        }
        admin.setEmail(txtEmail.getText());
        //phone number
        if (!AdminUtil.isPhoneNumber(txtPhoneNumber.getText())) {
            JOptionPane.showMessageDialog(null, "Phone Number Format Error!");
            return;
        }
        admin.setPhoneNumber(txtPhoneNumber.getText());
        //dob
        StringJoiner dobSj = new StringJoiner("-");
        String dobStr = dobSj.add(txtDOBYear.getText()).add(txtDOBMonth.getText()).add(txtDOBDay.getText()).toString();
        if (!AdminUtil.isDOB(dobStr)) {
            JOptionPane.showMessageDialog(null, "DOB Format Error!");
            return;
        }
        admin.setDob(dobStr);
        //gender
        admin.setGender(lblGender.getText());
        //save to file
        admin.update(admin);
    }//GEN-LAST:event_btnUpdateProfile1ActionPerformed

