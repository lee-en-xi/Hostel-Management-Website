    private void txtDOBDayKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDOBDayKeyPressed
        String day = txtDOBDay.getText();
        int len = day.length();
        char c = evt.getKeyChar();
        if(Character.isDigit(c)){
            if (len < 2) {
                txtDOBDay.setEditable(true);
            } else {
                txtDOBDay.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                txtDOBDay.setEditable(true);
            } else {
                txtDOBDay.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtDOBDayKeyPressed

