    private void txtDOBDayFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDOBDayFocusGained
        if (txtDOBDay.getText().equals("dd")) {
            txtDOBDay.setText("");
            txtDOBDay.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_txtDOBDayFocusGained

    private void txtDOBDayKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDOBDayKeyPressed
        int len = txtDOBDay.getText().length();
        char c = evt.getKeyChar();
        if (Character.isDigit(c)) {
            if (len < 2) {
                txtDOBDay.setEditable(true);
            } else {
                txtDOBDay.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                txtDOBDay.setEditable(true);
            } else {
                txtDOBDay.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtDOBDayKeyPressed

