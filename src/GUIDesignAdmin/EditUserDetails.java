    private User createRoleInstance(String role) {
        User u = null;
        switch (role) {
            case "customer":
                u = new Customer();
                break;
            case "vendor":
                u = new Vendor();
                break;
            case "runner":
                u = new Runner();
                break;
            default:
                break;
        }
        return u;
    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        User u = null;
        String userID = txtUserID.getText();
        FileHandlerImp fileHandlerImp = FileHandlerImp.getFileHandlerImpInstance();
        String filePath = "src/textFile/user.txt";
        String line = fileHandlerImp.readFileLine(filePath, userID);
        if (line == null) {
            return;
        }
        String[] data = line.split(":::"); //0-id, 1-name, 2-pw, 3-email, 4-phone.no, 5-dob, 6-gender, 7-role
        //Determine the role of the user
        System.out.println(data[7].trim());
        u = createRoleInstance(data[7].trim());

        //Setting the value of the user
        //id
        u.setUserID(userID);
        //name
        String username = txtName.getText();
        if (username.isEmpty()) {
            u.setUsername(data[1]);
        } else if (AdminUtil.isUsername(username)) {
            u.setUsername(username);
        } else {
            txtName.setText("");
            JOptionPane.showMessageDialog(null, "Username Must Be All Alphabets");
        }
        //password
        u.setPassword(data[2]);
        // email
        String email = txtEmail.getText();
        if (email.isEmpty()) {
            u.setEmail(data[3]);
        } else if (AdminUtil.isEmail(email)) {
            u.setEmail(email);
        } else {
            txtEmail.setText("");
            JOptionPane.showMessageDialog(null, "Email Format Is Invalid");
        }
        // phone number
        String phoneNumber = txtPhoneNumber.getText();
        if (phoneNumber.isEmpty()) {
            u.setPhoneNumber(data[4]);
        } else if (AdminUtil.isPhoneNumber(phoneNumber)) {
            u.setPhoneNumber(phoneNumber);
        } else {
            txtPhoneNumber.setText("");
            JOptionPane.showMessageDialog(null, "Phone Number Format Invalid");
        }
        //dob
        String year = txtDOBYear.getText();
        String month = txtDOBMonth.getText();
        String day = txtDOBDay.getText();
        String dobStr = year + "-" + month + "-" + day;
        boolean isFilledYear = !year.equals("yyyy");
        boolean isFilledMonth = !month.equals("MM");
        boolean isFilledDay = !day.equals("dd");
        if (!isFilledYear && !isFilledMonth && !isFilledDay) {
            u.setDob(data[5]);
        } else {
            if (isFilledYear && isFilledMonth && isFilledDay) {
                if (AdminUtil.isDOB(dobStr)) {
                    u.setDob(dobStr);
                } else {
                    JOptionPane.showMessageDialog(null, "DOB Format Is Invalid");
                }
            } else {
                JOptionPane.showMessageDialog(null, "All the Columns of the DOB Components Must Be Filled");
            }
        }
        //gender
        u.setGender(data[6]);
        //role
        u.setRole(data[7]);

        //validate
        if (txtUserID.getText().startsWith("c")) {
            if (cbRole.getSelectedItem().toString().equals("customer")) {
                Customer customer = Customer.readCustomerProfile(userID);
                Customer c = (Customer) u;
                c.setBalance(customer.getBalance());
                admin.update(c);
            } else {
                JOptionPane.showMessageDialog(null, "Wrong role");
                return;
            }
        } else if (txtUserID.getText().startsWith("r")) {
            if (cbRole.getSelectedItem().toString().equals("runner")) {
                Runner runner = Runner.readRunnerProfile(userID);
                Runner r = (Runner) u;
                r.setWorkStatus(runner.getWorkStatus());
                admin.update(r);
            } else {
                JOptionPane.showMessageDialog(null, "Wrong role");
                return;
            }
        } else if (txtUserID.getText().startsWith("v")) {
            if (cbRole.getSelectedItem().toString().equals("vendor")) {
                Vendor v = (Vendor) u;
                admin.update(v);
            } else {
                JOptionPane.showMessageDialog(null, "Wrong role");
                return;
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // Assuming you have validation methods and user creation logic in place
        String role = cbRole.getSelectedItem().toString();
        String userID = AdminUtil.generateID(role); // Generate new user ID
        String username = txtName.getText();
        String email = txtEmail.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String dobStr = txtDOBYear.getText() + "-" + txtDOBMonth.getText() + "-" + txtDOBDay.getText();
        String gender = (String) cbGender.getSelectedItem();

        // Validate inputs
        if (!AdminUtil.isUsername(username)) {
            JOptionPane.showMessageDialog(null, "Invalid Username");
            return;
        }
        if (!AdminUtil.isEmail(email)) {
            JOptionPane.showMessageDialog(null, "Invalid Email Format");
            return;
        }
        if (!AdminUtil.isPhoneNumber(phoneNumber)) {
            JOptionPane.showMessageDialog(null, "Invalid Phone Number");
            return;
        }
        if (!AdminUtil.isDOB(dobStr)) {
            JOptionPane.showMessageDialog(null, "Invalid Date of Birth");
            return;
        }

        // Create and set User object
        User newUser = createRoleInstance(role);
        newUser.setRole(role);
        newUser.setUserID(userID);
        newUser.setUsername(username);
        newUser.setEmail(email);
        newUser.setPhoneNumber(phoneNumber);
        newUser.setDob(dobStr);
        newUser.setGender(gender);

        admin.register(newUser);
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String userID = txtUserID.getText();
        if (userID.isEmpty() || !AdminUtil.isId(userID)) {
            JOptionPane.showMessageDialog(null, "Invalid UserID");
            return;
        }

        admin.delete(userID);
        if (userID != null && userID.matches("v\\d{6}")) {
            admin.dltstorename(userID);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtUserIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserIDKeyPressed
        int len = txtUserID.getText().length();
        if (len > 7 && evt.getExtendedKeyCode() != KeyEvent.VK_BACK_SPACE) {
            txtUserID.setEditable(false);
            return;
        }
        txtUserID.setEditable(true);
    }//GEN-LAST:event_txtUserIDKeyPressed

