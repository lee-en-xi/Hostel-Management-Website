    private void txtAmountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAmountKeyTyped
        char c = evt.getKeyChar();
        String currentText = txtAmount.getText().replaceAll("\\.", "");

        // Check for digit input
        if (Character.isDigit(c)) {
            // Remove leading zeros before adding a new digit
            currentText = removeLeadingZeros(currentText);
            String potentialText = currentText + c;        
            if (exceedsLimit(potentialText)) {
                evt.consume();
                return;
            }
            txtAmount.setText(formatText(potentialText));
            evt.consume();
        }
        // Handle backspace
        if (c == KeyEvent.VK_BACK_SPACE) {
            if (!currentText.equals("000")) {
                txtAmount.setText(formatText(currentText.substring(0, currentText.length() - 1)));
            }
            evt.consume();
        }
    }//GEN-LAST:event_txtAmountKeyTyped

    private void txtUserIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserIDKeyPressed
        int len = txtUserID.getText().length();
        if (len <=  7) {
            txtUserID.setEditable(true);
        } else {
            if (evt.getKeyChar() == KeyEvent.VK_BACK_SPACE) {
                txtUserID.setEditable(true);
            }
            else {
                txtUserID.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtUserIDKeyPressed

    private void btnTopUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTopUpActionPerformed
        if (!txtAmount.getText().equals("Maximum RM 1000 in a single top-up")) {
            String userID = txtUserID.getText();
            String topUpAmountStr = txtAmount.getText();
            double topUpAmount = Double.parseDouble(topUpAmountStr);
            FileHandlerImp fileHandlerImp = FileHandlerImp.getFileHandlerImpInstance();
            //user not exists
            if (fileHandlerImp.readFileLine("src/textFile/customer.txt", userID) == null) {
                JOptionPane.showMessageDialog(null, "Customer (id:" + userID + ") Not Found");
                return;
            }
            admin.topUp(userID, topUpAmount);
        } else {
            JOptionPane.showMessageDialog(null, "Please enter the top- up amount");
        }
    }//GEN-LAST:event_btnTopUpActionPerformed

