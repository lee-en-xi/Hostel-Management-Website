    private void btnGenerateReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateReceiptActionPerformed
        transaction = null;
        String transactionID = txtTransactionID.getText();
        FileHandlerImp fileHandlerImp = FileHandlerImp.getFileHandlerImpInstance();
        String line = fileHandlerImp.readFileLine("src/textFile/transaction.txt", transactionID);
        if(line.equals("false")) {
            JOptionPane.showMessageDialog(null, "TransactionID Wrong", "Warning(Transaction)", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String[] transactionData = line.split(":::");
        String senderID = transactionData[1];
        String receiverID = transactionData[2];
        double amount = Double.parseDouble(transactionData[3]);
        String time = transactionData[4];
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        LocalDateTime ldt = LocalDateTime.parse(time, formatter);
        
        String description = transactionData[5];

        transaction = new Transaction(transactionID, senderID, receiverID, amount, ldt, description);

        String receiptStr = transaction.getReceipt();
        
        String title = "Receipt";
        String message = receiptStr.replace("<newline>", "\n");
        JOptionPane.showMessageDialog(null, message, title, JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_btnGenerateReceiptActionPerformed

    private void btnSendReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendReceiptActionPerformed
        if (transaction == null) {
            JOptionPane.showMessageDialog(null, "TransactionID Wrong", "Warning(Transaction)", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Notification notification = new Notification(adminID, transaction.getSenderID(), "Receipt (" + transaction.getTransactionID() + ")", transaction.getReceipt());
        Notification.sendNotification(notification, "notification.txt");
        txtTransactionID.setText("");
    }//GEN-LAST:event_btnSendReceiptActionPerformed

