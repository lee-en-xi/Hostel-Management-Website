    private void addToTable(String role) {
        List<String[]> dataList = admin.readUsers(role);
        
        if(dataList.isEmpty()) {
            return;
        }
        
        String[] columns = dataList.get(0);
        
        DefaultTableModel model = (DefaultTableModel)tblReadUser.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        
        for (String column : columns) {
            model.addColumn(column);
        }
        
        for (int i = 1; i < dataList.size(); i++) {
            String[] row = dataList.get(i);
            if (row != null && row.length > 0 && !isRowEmpty(row)) {
                model.addRow(row);
            }
        }
    }
    
    // Helper method to check if a row is empty
    private boolean isRowEmpty(String[] row) {
        for (String cell : row) {
            if (cell != null && !cell.trim().isEmpty()) {
                return false; // If any cell is non-empty, the row is not empty
            }
        }
        return true; // If all cells are empty, the row is empty
    }
    
    private void resetTable() {
        tblReadUser.setModel(originalTableModel);
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(originalTableModel);
        tblReadUser.setRowSorter(sorter);
    }
