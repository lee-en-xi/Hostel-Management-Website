    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        Review r = new Review();
        if (cbItem.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Please select an item");
            return;
        }
        String itemId = cbItem.getSelectedItem().toString().split("-")[0];
        String review = txtReview.getText();
        String rate = spRating.getValue().toString();
        boolean b = r.writeReviews(storeId, itemId, customerId, review, rate);
        if (b) {
            JOptionPane.showMessageDialog(null, "Review recorded successfully\nThanks for your feedback");
            cbItem.setSelectedIndex(-1);
            txtReview.setText("");
            spRating.setValue(5);
        }
    }//GEN-LAST:event_btnDoneActionPerformed

