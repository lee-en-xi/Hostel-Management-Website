    private void addToComboBox() {
        List<String[]> menuList = Menu.readMenu(storeId);
        List<String> list = new ArrayList<>();
        for (String[] menuItem : menuList) {
            if(orderDetail.contains(menuItem[1].trim())) {
                String str = menuItem[0] + "-" + menuItem[1];
                list.add(str);
                cbItem.addItem(str);
            }
        }
        orderedItemList = list;
    }
    
