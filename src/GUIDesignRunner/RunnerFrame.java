    private void taskHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_taskHistoryActionPerformed
        // Remove all the components in taskList
        HistoryList.removeAll();

        // Switch Pages and Button Colour
        resetBtnColor();
        taskHistory.setBackground(new Color(218, 106, 0));

        HistoryList.setLayout(new BoxLayout(HistoryList, BoxLayout.PAGE_AXIS));
        Boolean have_record = false;

        // Extract RunnerTask Data
        List<String[]> runnerTaskData = fileHandlerImp.readUntilFindFileLine(runnerTaskFilePath, inputRunnerID);

        if (runnerTaskData == null) {
            setAllInvisible();
            Task_History.setVisible(true);
            return;
        }

        for (String[] lines : runnerTaskData) {
            if (!lines[3].equals("Unspecified") && !lines[3].equals("NoResponse")) {
                have_record = true;

                String taskID = lines[2];
                String response = lines[3];
                String pickUpTime = lines[4];
                String deliveryTime = lines[5];
                String taskStatus = lines[6];

                String taskDatas = fileHandlerImp.readFileLine(taskFilePath, taskID);
                String[] taskData = taskDatas.split(":::");

                String orderID = taskData[1];
                String pickUpAddress = taskData[2];
                String destinationAddress = taskData[3];
                String shopName = taskData[6];
                String deliveryFee = taskData[7];
                String customerName = taskData[4];

                String[] details = {taskID, pickUpTime, deliveryTime, taskStatus, orderID,
                    pickUpAddress, destinationAddress, shopName, deliveryFee, customerName, response};

                //Create Panel and add into TaskList
                TaskDetailsPanel taskPanel = new TaskDetailsPanel(details);
                HistoryList.add(taskPanel);
            }
        }

        // Set the preferred size of tshe taskList to match the total height
        HistoryList.setPreferredSize(new Dimension(HistoryList.getPreferredSize().width, HistoryList.getPreferredSize().height));

        history_scrollpane.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
        history_scrollpane.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_NEVER);

        // Remove any existing components from the viewport of scrollPane and add the taskList
        JViewport viewPort = history_scrollpane.getViewport();
        viewPort.removeAll();
        viewPort.add(HistoryList);

        // Ensure the view is updated
        viewPort.revalidate();
        viewPort.repaint();

        //Switch Panel to Current_Task4
        setAllInvisible();
        if (have_record) {
            Task_History2.setVisible(true);
        } else {
            Task_History.setVisible(true);
        }
    }//GEN-LAST:event_taskHistoryActionPerformed

