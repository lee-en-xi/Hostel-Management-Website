    private void currentTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_currentTaskActionPerformed
        // Set up taskList
        taskList.removeAll();
        taskList.setLayout(new BoxLayout(taskList, BoxLayout.PAGE_AXIS));

        // Switch Pages and Button Colour
        setAllInvisible();
        resetBtnColor();
        currentTask.setBackground(new Color(218, 106, 0));

        Boolean have_available_task = false;
        Boolean have_current_task = false;

        List<String[]> runnerTaskDatas = fileHandlerImp.readUntilFindFileLine(runnerTaskFilePath, inputRunnerID);

        if (runnerTaskDatas == null) {
            setAllInvisible();
            Current_Task3.setVisible(true);
            return;
        }

        for (String[] runnerTaskData : runnerTaskDatas) {
            if ((runnerTaskData[3].equals("Unspecified")) && (runnerTaskData[6].equals("Unspecified"))) {
                //Show the Current_Task2
                have_available_task = true;

                // Extract Task Details based on TaskID stored in RunnerTask
                String taskDatas = fileHandlerImp.readFileLine(taskFilePath, runnerTaskData[2]);
                String[] taskData = taskDatas.split(":::");

                // Create Panel and add into TaskList
                TaskPanel taskPanel = new TaskPanel(taskData[0], taskData[1], taskData[2], taskData[3], taskData[4], taskData[5], taskData[6], taskData[7], taskData[8], taskData[9]);
                taskPanel.addActionListener(taskPanel);
                taskList.add(taskPanel);
            }

            if ((runnerTaskData[3].equals("Accept")) && !(runnerTaskData[6].equals("Delivered"))) {
                have_current_task = true;
                selectedTaskID = runnerTaskData[2];
            }
        }

        //Detect These Condition in sequance 
        //1.On/Off Duty 
        //2.Whether Have Current Task 
        //3.Whether Have Available Task
        String runnerDatas = fileHandlerImp.readFileLine(runnerFilePath, inputRunnerID);
        String[] runnerData = runnerDatas.split(":::");
        if (runnerData[3].equals("offDuty")) {
            Current_Task2.setVisible(true);
        } else {
            if (have_current_task) {
                // Extract Task Details based on TaskID stored in RunnerTask
                String taskDatas = fileHandlerImp.readFileLine(taskFilePath, selectedTaskID);
                String[] taskData = taskDatas.split(":::");

                //Loop the Task Data to find the relevant Details based on the TaskID (PK)
                if (taskData.length != 0) {
                    for (int k = 0; k < taskData.length; k++) {
                        if (taskData[0].equals(selectedTaskID)) {
                            //Store the Current Task Details to Global Variable for Use of Panel
                            thisTaskID = taskData[0];
                            thisOrderID = taskData[1];
                            thisPickUpAddress = taskData[2];
                            thisDestinationAddress = taskData[3];
                            thisCustomerName = taskData[4];
                            thisCustomerID = taskData[5];
                            thisShopName = taskData[6];
                            thisDeliveryFee = taskData[7];
                            thisPickUpTime = taskData[8];
                            thisDeliveryTime = taskData[9];

                        }
                    }
                }

                //Get Task Details from Global Variable to Set Label Text
                taskTaskIDLabel1.setText(thisTaskID);
                taskOrderIDLabel1.setText(thisOrderID);
                taskPickUpAddressLabel1.setText(thisPickUpAddress);
                taskDestinationAddressLabel1.setText(thisDestinationAddress);
                taskCustomerNameLabel1.setText(thisCustomerName);
                taskCustomerIDLabel1.setText(thisCustomerID);
                taskShopNameLabel1.setText(thisShopName);
                taskDeliveryFeeLabel1.setText(thisDeliveryFee);
                taskPickUpTimeLabel1.setText(thisPickUpTime);
                taskDeliveryTimeLabel1.setText(thisDeliveryTime);

                //Show Current_Task4
                Current_Task4.setVisible(true);

                //Reload Slider
                ReloadSlider();
            } else {
                if (have_available_task) {
                    Current_Task.setVisible(true);
                } else {
                    Current_Task3.setVisible(true);
                }
            }
        }

        currentTask.setBackground(new Color(218, 106, 0));

        // Set the preferred size of the taskList to match the total height
        taskList.setPreferredSize(new Dimension(taskList.getPreferredSize().width, taskList.getPreferredSize().height));

        // Remove any existing components from the viewport of scrollPane and add the taskList
        JViewport viewPort = jScrollPane.getViewport();
        viewPort.removeAll();
        viewPort.add(taskList);

        // Ensure the view is updated
        viewPort.revalidate();
        viewPort.repaint();

    }//GEN-LAST:event_currentTaskActionPerformed

