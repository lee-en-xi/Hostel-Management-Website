    private void mainPageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mainPageActionPerformed
        //Switch Pages and Button Colour
        setAllInvisible();
        resetBtnColor();
        Main_Page.setVisible(true);
        mainPage.setBackground(new Color(218, 106, 0));

        //Find input RunnerID relevant rows index from Data
        String runnerDatas = fileHandlerImp.readFileLine(runnerFilePath, inputRunnerID);

        //Set Warning Label Visible to Default "false"
        warning_label.setVisible(false);

        //Check Whether Have Current Task
        Boolean have_current_task = false;
        List<String[]> runnerTaskData = fileHandlerImp.readUntilFindFileLine(runnerTaskFilePath, inputRunnerID);
        if (runnerTaskData != null) {
            for (String[] dataline : runnerTaskData) {
                if ((dataline[3].equals("Accept")) && !(dataline[6].equals("Delivered"))) {
                    have_current_task = true;
                }
            }
        }

        //Store relevant details into Gloabl Variable
        if (runnerDatas != null) {
            String[] runnerData = runnerDatas.split(":::");

            thisAccountIDLabel = runnerData[0];
            thisNameLabel = runnerData[1];
            thisEmailLabel = runnerData[2];
            thisWorkStatusLabel = runnerData[3];

            if (have_current_task) {
                onDutyBtn.setEnabled(false);
                offDutyBtn.setEnabled(false);
                warning_label.setVisible(true);

            } else {
                onDutyBtn.setEnabled(true);
                offDutyBtn.setEnabled(true);
            }
        } else {

            thisAccountIDLabel = "Not Found";
            thisNameLabel = "Not Found";
            thisAgeLabel = "Not Found";
            thisEmailLabel = "Not Found";
            thisWorkStatusLabel = "Not Found";

            onDutyBtn.setEnabled(false);
            offDutyBtn.setEnabled(false);
        }

        //Load Global Variable into jLabel
        accountIDLabel.setText(thisAccountIDLabel);
        nameLabel.setText(thisNameLabel);
        emailLabel.setText(thisEmailLabel);
        workStatusLabel.setText(thisWorkStatusLabel);

    }//GEN-LAST:event_mainPageActionPerformed

